function [x1, x2] = biquad(a1,a2,b1,b2,d1,d2,...
    e1,e2,f1,f2,g1,g2,h1,h2,i1,i2,j1,j2)

syms x1 x2
equ1 = (a1*x1^2+b1*x1+d1)*x2^2 ...
        +(e1*x1^2+f1*x1+g1)*x2 ...
      +(h1*x1^2+i1*x1+j1) == 0;
equ2 = (a2*x1^2+b2*x1+d2)*x2^2 ...
         +(e2*x1^2+f2*x1+g2)*x2 ...
         +(h2*x1^2+i2*x1+j2) == 0;
[x1,x2] = vpasolve([equ1,equ2],[x1,x2]);
end